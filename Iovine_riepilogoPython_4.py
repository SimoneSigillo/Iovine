#Prendi In input due numeri e restituiscili ordinati, prima il più piccolo e
#poi più grande.
numeroUno = int(input("Inserisci il primo numero: "))
numeroDue = int(input("Inserisci il secondo numero: "))
if numeroUno < numeroDue:
    print(numeroUno)
    print(numeroDue)
else:
    print(numeroDue)
    print(numeroUno)
